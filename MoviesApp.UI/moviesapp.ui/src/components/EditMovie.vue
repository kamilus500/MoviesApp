<template>
    <h1>Edit Movie</h1>
    <form @submit.prevent="handleSubmit">
      <div class="field-container">
        <label for="title">Title:</label>
        <input v-model="form.title" type="text" id="title" placeholder="Edit title">
        <span v-if="v$.title.$error">{{ v$.title.$errors[0].$message }}</span>
      </div>
      <div class="field-container">
        <label for="director">Director:</label>
        <input v-model="form.director" type="text" id="director" placeholder="Edit director">
        <span v-if="v$.director.$error">{{ v$.director.$errors[0].$message }}</span>
      </div>
      <div class="field-container">
        <label for="year">Year:</label>
        <input v-model="form.year" type="number" id="year" placeholder="Edit year">
        <span v-if="v$.year.$error">{{ v$.year.$errors[0].$message }}</span>
      </div>
      <div class="field-container">
        <label for="rate">Rate:</label>
        <input v-model="form.rate" type="number" id="rate" placeholder="Edit rate">
        <span v-if="v$.rate.$error">{{ v$.rate.$errors[0].$message }}</span>
      </div>
      <button type="submit">Save Changes</button>
    </form>
  </template>
  
  <script>
  import { reactive, computed, onMounted } from 'vue';
  import { useVuelidate } from '@vuelidate/core';
  import { required } from '@vuelidate/validators';
  import MovieService from '@/services/MovieService';
  
  export default {
    props: {
      movieId: {
        type: Number,
        required
      }
    },
    setup(props, { emit }) {
      const form = reactive({
        title: '',
        director: '',
        year: 0,
        rate: 0
      });
  
      const rules = computed(() => ({
        title: { required },
        director: { required },
        year: { required },
        rate: { required }
      }));
  
      const v$ = useVuelidate(rules, form);
  
      const fetchMovie = async () => {
        try {
          const response = await MovieService.getById(props.movieId);
          const movie = response.data;
          form.title = movie.title;
          form.director = movie.director;
          form.year = movie.year;
          form.rate = movie.rate;
        } catch (error) {
          console.error('Error fetching movie:', error);
        }
      };

      const handleSubmit = async () => {
        const result = v$.value.$validate();
        if (!result) {
          return;
        }
  
        await MovieService.update({
          id: props.movieId,
          title: form.title,
          director: form.director,
          year: form.year,
          rate: form.rate
        });

        emit('close-editDialog');
      };
  
      onMounted(fetchMovie);
  
      return {
        form,
        v$,
        handleSubmit
      };
    }
  };
  </script>
  
